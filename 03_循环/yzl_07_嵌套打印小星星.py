# 用嵌套输出五行小星星，每行小星星递增

# 首先输出五行小星星
row = 1

while row <= 5:
    # 每行要打印的星星数跟行数一样
    # 增加一个小的循环，负责每行中每‘列’小星星的显示
    # 1.定义一个列计数变量
    col = 1
    # 2.开始循环
    while col <= row:

        print("*", end="")

        col += 1
    # print("第%d行" % row)
    # 这行代码是为了增加换行，上面用了不换行
    print("")

    row += 1

